// 这是一个简化版的 ZIP 处理函数
class SimpleZip {
  constructor() {
    this.files = new Map();
  }

  file(name, content) {
    this.files.set(name, content);
  }

  async generateAsync(options) {
    const zip = new Blob(
      Array.from(this.files.entries()).map(([name, content]) => {
        return new Blob([content]);
      })
    );
    return zip;
  }
}

// 替代 JSZip
window.JSZip = SimpleZip;
